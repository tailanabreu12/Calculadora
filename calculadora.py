number_1 = int(input('Entre com o primeiro numero: '))
number_2 = int(input('Entre com o segundo numero: '))

print('{} + {} = '.format(number_1, number_2))
print(number_1 + number_2)

#subtração
print('{} - {} = '.format(number_1, number_2))
print(number_1 - number_2)

#multiplicação
print('{} * {} = '.format(number_1, number_2))
print(number_1 * number_2)

#divisão
print('{} / {} = '.format(number_1, number_2))
print(number_1 * number_2)